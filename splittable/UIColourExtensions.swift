import UIKit

extension UIColor {
    
    func lightBlue() -> UIColor {
        let lightBlue = UIColor(red: 0.0/255.0, green: 193.0/255.0, blue: 255.0/255.0, alpha: 1.0)
        return lightBlue
    }
}

